    /***********************************************
求解a和b的最大公约数
************************************************
@接口: long long gcd (long long a, long long b)
@功能: 求解a和b的最大公约数
@时间复杂度: O (5 * log (n))
@a: 较大的数
@b: 较小的数
************************************************/

long long gcd(long long a, long long b) {
    return (!b)? a: gcd(b, a % b);
}
